    private String seleccionarArchivo()
    {
        File archivo=null;
        String valorArchivo="";
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Base de datos", "xls");
        fileChooser.setFileFilter(filter);
        fileChooser.setDialogTitle("Abrir archivo...");
        File ruta = new File("D:/Dropbox/Archivos del análisis/alumnos clasificados por su rendimiento/clasificacion 60 20 20");
        fileChooser.setCurrentDirectory(ruta);
        int result = fileChooser.showOpenDialog(null);
        if(result==JFileChooser.APPROVE_OPTION)
            {
                archivo= fileChooser.getSelectedFile();
                valorArchivo= archivo.toString();
            }
        return valorArchivo;
}
    
