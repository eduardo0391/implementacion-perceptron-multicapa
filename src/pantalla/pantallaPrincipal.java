    private XYSeries cargarSerie(List lista, String nombre)
    {
        XYSeries auxiliarSeries= new XYSeries(nombre.toString());
        for (int i=0; i<lista.size();i++)
            auxiliarSeries.add(i,(Float)lista.get(i));
        return auxiliarSeries;
    }
    
